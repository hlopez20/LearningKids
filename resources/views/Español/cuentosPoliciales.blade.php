                    Por norma general tienen que ver con la resolución de un delito.

                    Dentro del género policial podemos distinguir dos tipos de narración, la blanca y la negra. En la narración blanca, la policía es correcta y cumple con su deber de atrapar al delincuente.

                    Por otro lado, en la narración negra, el policía se infiltra en las líneas de la delincuencia y se adentra en el grupo delictivo para conseguir al criminal.
