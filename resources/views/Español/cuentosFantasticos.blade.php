                            Un día, los dos hermanos estaban jugando delante de su casa con otros niños, en un prado.
                            Junto al prado había un estanque que llegaba hasta uno de los lados de la casa.
                            Los niños corrían y jugaban a alcanzarse, y cantaban:

                            Patito, quiéreme un poquito, y te daré mi pajarito. El pajarito me buscará una pajita;
                            la paja la daré a mi vaquita; la vaca me dará leche rica; la leche la daré al pastelero; el pastelero me cocerá pasteles; los pasteles los daré al gatito; el gato me cazará ratoncitos; los ratoncitos los colgaré a la espalda... ¡y te morderán!.

