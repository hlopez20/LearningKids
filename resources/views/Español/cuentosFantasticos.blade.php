                            La madrastra, al verlos desde la ventana tan felices, se enfadó y, como era bruja, encantó a los dos hermanitos,
                            convirtiendo, al niño, en pez, y a la niña, en cordero. Así, el pez quedó nadando
                            tristemente en el estanque mientras el corderillo corría por el prado, triste también. De este modo pasó algún tiempo.

                            Un día llegaron forasteros al palacio, y la malvada madrastra llamó al cocinero y le dijo:

                            - Ve al prado a buscar el cordero y mátalo, pues no tenemos nada para ofrecer a los huéspedes.
                            Bajó el cocinero, cogió al animalito, y se lo llevó a la cocina, atado de patas.
                            Pero cuando el hombre, sacando el cuchillo, salió al umbral para afilarlo, reparó en un pececito que,
                            con muestras de gran agitación, nadaba frente al vertedero y lo miraba. Era el hermanito,
                            que, al ver que el cocinero se llevaba al corderillo, había acudido desde el centro del estanque.

                            Baló entonces el corderillo desde arriba:

                            -Hermanito que moras en el estanque, mi pobre alma, dolida está y sangrante.
                            Muy pronto el cocinero sin compasión, me clavará el cuchillo en el corazón.

